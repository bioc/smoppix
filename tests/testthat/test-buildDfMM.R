    expect_error(buildDfMM(objBG, gene = "gene1_gene2", i = "nn"))
    expect_error(buildDfMM(objBG, gene = "gene1", i = "nnPair"))
    expect_error(buildDfMM(objBG, gene = c("gene1", "gene2", "gene3"), pi = "nnPair", hypFrame = hypFrame2))
