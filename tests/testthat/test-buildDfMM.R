    expect_error(buildDfMM(objBG, gene = "protein1", pi = "nn"))
    expect_error(buildDfMM(objCSR2, gene = "gene1", pi = "allDist"))
    expect_error(buildDfMM(objCSR2, gene = "gene1--gene2", pi = "nnPair"))
