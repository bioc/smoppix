context("Test building of dataframes of probabilistic indices for mixed model building")
test_that("Building data frames for mixed modelling proceeds without errors", {
    expect_s3_class(dfBG1 <- buildDfMM(piEstsBG, gene = "gene1", hypFrame = hypFrame2, pi = "nn"), "data.frame")
    expect_s3_class(dfBG2 <- buildDfMM(piEstsBG, gene = "gene1--gene2", hypFrame = hypFrame2, pi = "nnPair"), "data.frame")
    expect_s3_class(dfBG2b <- buildDfMM(piEstsBG, gene = c("gene1", "gene2"), hypFrame = hypFrame2, pi = "nnPair"), "data.frame")
    expect_identical(dfBG2, dfBG2b)
    expect_s3_class(dfBG3 <- buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "gene1", pi = "allDist"), "data.frame")
    expect_s3_class(dfBG4 <- buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "gene1--gene2", pi = "allDistPair"), "data.frame")
    expect_s3_class(dfBG5 <- buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "gene1", pi = "midpoint"), "data.frame")
    expect_s3_class(dfBG6 <- buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "gene10", pi = "fixedpoint"), "data.frame")
    expect_s3_class(dfBG7 <- buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "gene1", pi = "edge"), "data.frame")
    expect_s3_class(dfCSR1 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene23", pi = "edge"), "data.frame")
    expect_s3_class(dfCSR2 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene2", pi = "fixedpoint"), "data.frame")
    expect_s3_class(dfCSR3 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene2", pi = "midpoint"), "data.frame")
    expect_s3_class(dfCSR4 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene2", pi = "allDist"), "data.frame")
    expect_s3_class(dfCSR5 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene20", pi = "nn"), "data.frame")
    expect_s3_class(dfCSR6 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene2--gene20", pi = "nnPair"), "data.frame")
    expect_s3_class(dfCSR7 <- buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene2--gene23", pi = "allDistPair"), "data.frame")
    expect_true(all(dfCSR7$pi > 0 & dfCSR7$pi < 1) && all(dfBG3$pi > 0 & dfBG3$pi < 1))
})
piEstsCSR2 <- estPims(hypFrame2, pis = c("nn"), features = c("gene1", "gene2"),
                     point = c(0.5, 0.5), null = "CSR")
test_that("Building data frames throws errors where appropriate", {
    expect_error(buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = c("gene1", "gene2"), pi = "nn"))
    expect_error(buildDfMM(piEstsCSR, hypFrame = hypFrame2, gene = "gene1", pi = "Kest"))
    expect_error(buildDfMM(piEstsBG, hypFrame = hypFrame2, gene = "protein1", pi = "nn"))
    expect_error(buildDfMM(piEstsCSR2, hypFrame = hypFrame2, gene = "gene1", pi = "allDist"))
    expect_error(buildDfMM(piEstsCSR2, hypFrame = hypFrame2, gene = "gene1--gene2", pi = "nnPair"))
    expect_error(buildDfMM(piEstsBG, gene = c("gene1", "gene2"), hypFrame = hypFrame2, pi = "nn"))
    expect_error(buildDfMM(piEstsBG, gene = "gene1_gene2", hypFrame = hypFrame2, pi = "nn"))
    expect_error(buildDfMM(piEstsBG, gene = "gene1", hypFrame = hypFrame2, pi = "nnPair"))
    expect_error(buildDfMM(piEstsBG, gene = c("gene1", "gene2", "gene3"), pi = "nnPair", hypFrame = hypFrame2))
})
